namespace VirtualMeetingExtensibility.Models
{
    /// <summary>
    /// One note information
    /// </summary>
    public class OneNoteInfo
    {
        /// <summary>
        /// Gets or sets chat id
        /// </summary>
        public string ChatId { get; set; }

        /// <summary>
        /// Gets or sets  created date
        /// </summary>
        public string CreatedDate { get; set; }

        /// <summary>
        /// Gets or sets notebook Name
        /// </summary>
        public string NotebookName { get; set; }

        /// <summary>
        /// Gets or sets noteBook id
        /// </summary>
        public string NoteBookId { get; set; }

        /// <summary>
        /// Gets or sets section Name
        /// </summary>
        public string SectionName { get; set; }

        /// <summary>
        /// Gets or sets section id
        /// </summary>
        public string SectionId { get; set; }

        /// <summary>
        /// Gets or sets page Name
        /// </summary>
        public string PageName { get; set; }

        /// <summary>
        /// Gets or sets page id
        /// </summary>
        public string PageId { get; set; }

        /// <summary>
        /// Gets or sets event id
        /// </summary>
        public string EventId { get; set; }
    }
}
